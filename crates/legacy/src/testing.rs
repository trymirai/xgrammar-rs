/// Get the ids of the rejected tokens from the bitmask. Mainly for debug purposes.
///
/// # Parameters
///
/// - `bitmask`: The rejected token bitmask. Should be generated by `allocate_token_bitmask`
///   and filled by `fill_next_token_bitmask`.
/// - `vocab_size`: The size of the vocabulary.
/// - `index`: The batch index of the bitmask.
///
/// # Returns
///
/// A boxed slice of rejected token ids.
pub fn get_masked_tokens_from_bitmask(
    bitmask: &DLTensor,
    vocab_size: i32,
    index: i32,
) -> Box<[i32]> {
    unsafe {
        let result = ffi::get_masked_tokens_from_bitmask(
            bitmask as *const _,
            vocab_size,
            index,
        );
        result.iter().copied().collect()
    }
}

/// Check if the bitmask is a single token bitmask.
///
/// # Parameters
///
/// - `bitmask`: The bitmask to check.
/// - `vocab_size`: The size of the vocabulary.
/// - `index`: The index of the bitmask.
///
/// # Returns
///
/// A tuple of `(is_single_token, token_id)`. `token_id` is -1 if not a single token.
pub fn is_single_token_bitmask(
    bitmask: &DLTensor,
    vocab_size: i32,
    index: i32,
) -> (bool, i32) {
    unsafe {
        let result = ffi::is_single_token_bitmask(
            bitmask as *const _,
            vocab_size,
            index,
        );
        (result.get_is_single(), result.get_token_id())
    }
}

pub fn regex_to_ebnf(
    regex: &str,
    with_rule_name: bool,
) -> Result<String, String> {
    cxx::let_cxx_string!(regex_cxx = regex);
    cxx::let_cxx_string!(error_out_cxx = "");
    let out = unsafe {
        ffi::regex_to_ebnf(
            &regex_cxx,
            with_rule_name,
            error_out_cxx.as_mut().get_unchecked_mut(),
        )
    };
    let err = error_out_cxx.to_string();
    if !err.is_empty() {
        return Err(err);
    }
    Ok(out.to_string())
}

pub fn traverse_draft_tree(
    retrieve_next_token: &DLTensor,
    retrieve_next_sibling: &DLTensor,
    draft_tokens: &DLTensor,
    matcher: &mut GrammarMatcher,
    bitmask: &mut CxxUniquePtr<DLTensor>,
) -> Result<(), String> {
    cxx::let_cxx_string!(error_out_cxx = "");
    let ok = unsafe {
        ffi::traverse_draft_tree(
            retrieve_next_token as *const _,
            retrieve_next_sibling as *const _,
            draft_tokens as *const _,
            matcher.ffi_mut(),
            bitmask.as_mut_ptr(),
            error_out_cxx.as_mut().get_unchecked_mut(),
        )
    };
    if !ok {
        return Err(error_out_cxx.to_string());
    }
    Ok(())
}

pub fn generate_range_regex(
    start: Option<i64>,
    end: Option<i64>,
) -> Result<String, String> {
    let has_start = start.is_some();
    let start_val = start.unwrap_or(0);
    let has_end = end.is_some();
    let end_val = end.unwrap_or(0);
    cxx::let_cxx_string!(error_out_cxx = "");
    let result = unsafe {
        ffi::generate_range_regex(
            has_start,
            start_val,
            has_end,
            end_val,
            error_out_cxx.as_mut().get_unchecked_mut(),
        )
    };
    let err = error_out_cxx.to_string();
    if !err.is_empty() {
        return Err(err);
    }
    Ok(result.to_string())
}

